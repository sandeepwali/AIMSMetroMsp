------------------------------
-- Processing M1 Job Repository --
------------------------------
CREATE TABLE BATCH_PROCESSING_M1_JOB_INSTANCE  (
	JOB_INSTANCE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 100 INCREMENT BY 1) NOT NULL PRIMARY KEY ,
		VERSION BIGINT ,
	JOB_NAME VARCHAR(100) NOT NULL,
	JOB_KEY VARCHAR(32) NOT NULL,
	constraint PROCESSING_M1_JOB_INST_UN unique (JOB_NAME, JOB_KEY)
) ;


CREATE TABLE BATCH_PROCESSING_M1_JOB_EXECUTION  (
    JOB_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 100 INCREMENT BY 1) NOT NULL PRIMARY KEY,
    VERSION BIGINT,
    JOB_INSTANCE_ID BIGINT NOT NULL,
    CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    START_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,
    END_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,
    STATUS VARCHAR(10),
    EXIT_CODE VARCHAR(2500),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    JOB_CONFIGURATION_LOCATION VARCHAR(2500) NULL,
    constraint PROCESSING_M1_JOB_INST_EXEC_FK foreign key (JOB_INSTANCE_ID)
    references BATCH_PROCESSING_M1_JOB_INSTANCE(JOB_INSTANCE_ID)
);


CREATE TABLE BATCH_PROCESSING_M1_JOB_EXECUTION_PARAMS  (
    JOB_EXECUTION_ID BIGINT NOT NULL,
    PARAMETER_NAME VARCHAR(250),
    PARAMETER_TYPE VARCHAR(250),
    PARAMETER_VALUE VARCHAR(250),
   
    IDENTIFYING CHAR(1) NOT NULL ,
    PRIMARY KEY (JOB_EXECUTION_ID,PARAMETER_NAME ),
    constraint PROCESSING_M1_JOB_EXEC_PARAMS_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_PROCESSING_M1_JOB_EXECUTION(JOB_EXECUTION_ID)
) ;


CREATE TABLE BATCH_PROCESSING_M1_STEP_EXECUTION  (
    STEP_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL PRIMARY KEY ,
    VERSION BIGINT ,
    STEP_NAME VARCHAR(100) ,
    JOB_EXECUTION_ID BIGINT ,
    START_TIME TIMESTAMP  ,
    END_TIME TIMESTAMP  ,
    STATUS VARCHAR(10) ,
    COMMIT_COUNT BIGINT ,
    READ_COUNT BIGINT ,
    FILTER_COUNT BIGINT ,
    WRITE_COUNT BIGINT ,
    READ_SKIP_COUNT BIGINT ,
    WRITE_SKIP_COUNT BIGINT ,
    PROCESS_SKIP_COUNT BIGINT ,
    ROLLBACK_COUNT BIGINT ,
    EXIT_CODE VARCHAR(2500) ,
    EXIT_MESSAGE VARCHAR(2500) ,
    LAST_UPDATED TIMESTAMP,
    CREATE_TIME TIMESTAMP,
    constraint PROCESSING_M1_JOB_EXEC_STEP_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_PROCESSING_M1_JOB_EXECUTION(JOB_EXECUTION_ID)
) ;


CREATE TABLE BATCH_PROCESSING_M1_STEP_EXECUTION_CONTEXT  (
    STEP_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT CLOB,
    constraint PROCESSING_M1_STEP_EXEC_CTX_FK foreign key (STEP_EXECUTION_ID)
    references BATCH_PROCESSING_M1_STEP_EXECUTION(STEP_EXECUTION_ID)
) ;
CREATE TABLE BATCH_PROCESSING_M1_JOB_EXECUTION_CONTEXT  (
    JOB_EXECUTION_ID BIGINT NOT NULL  PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT CLOB,
    constraint PROCESSING_M1_JOB_EXEC_CTX_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_PROCESSING_M1_JOB_EXECUTION(JOB_EXECUTION_ID)
) ;


CREATE TABLE BATCH_PROCESSING_M1_STEP_EXECUTION_SEQ (
	 ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL PRIMARY KEY
);
CREATE TABLE BATCH_PROCESSING_M1_JOB_EXECUTION_SEQ (
	 ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 10 INCREMENT BY 1) NOT NULL PRIMARY KEY
);
CREATE TABLE BATCH_PROCESSING_M1_JOB_SEQ (
	 ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1)NOT NULL  PRIMARY KEY
);